"""Summarize batch_results.json into a table with one task header row and three trial sub-rows.

Usage:
    # Default: looks for batch_results.json in claw-eval/traces/<latest>/
    python scripts/summarize_results.py

    # Specify input file directly
    python scripts/summarize_results.py --input /path/to/batch_results.json

    # Use a claw-eval config YAML to resolve base dirs
    python scripts/summarize_results.py --config claw-eval/config_general.yaml

    # Include LLM failure analysis from generate_trial_reports.py output
    python scripts/summarize_results.py \
        --input /path/to/batch_results.json \
        --report-dir /path/to/reports/

    # Output as CSV to file
    python scripts/summarize_results.py --format csv --output results.csv
"""

import argparse
import csv
import io
import json
import os
import sys
from pathlib import Path

try:
    import yaml
except ImportError:
    yaml = None

SCRIPT_DIR = Path(__file__).resolve().parent
REPO_DIR = SCRIPT_DIR.parent
DEFAULT_BASE_DIR = REPO_DIR


def find_latest_trace_dir(base: Path) -> Path | None:
    """Find the most recently modified directory under base/traces/."""
    traces = base / "claw-eval" / "traces"
    if not traces.is_dir():
        return None
    dirs = [d for d in traces.iterdir() if d.is_dir()]
    if not dirs:
        return None
    return max(dirs, key=lambda d: d.stat().st_mtime)


def resolve_input(args) -> Path:
    """Resolve the batch_results.json path from args."""
    if args.input:
        return Path(args.input)

    base = DEFAULT_BASE_DIR
    if args.config:
        config_path = Path(args.config)
        if yaml is None:
            print("Error: pyyaml is required for --config. Install with: pip install pyyaml", file=sys.stderr)
            sys.exit(1)
        with open(config_path) as f:
            cfg = yaml.safe_load(f) or {}
        defaults = cfg.get("defaults", {})
        trace_dir_name = defaults.get("trace_dir", "traces")
        # config file is under claw-eval/, so trace_dir is relative to its parent
        base = config_path.parent.parent
        traces = base / trace_dir_name
        if traces.is_dir():
            dirs = [d for d in traces.iterdir() if d.is_dir()]
            if dirs:
                latest = max(dirs, key=lambda d: d.stat().st_mtime)
                return latest / "batch_results.json"

    # Fallback: find latest trace dir under repo
    latest = find_latest_trace_dir(base)
    if latest:
        return latest / "batch_results.json"

    print("Error: no input file specified and no trace directories found.", file=sys.stderr)
    print("  Use --input to specify batch_results.json path.", file=sys.stderr)
    sys.exit(1)


def load_data(path):
    with open(path) as f:
        return json.load(f)


def load_reports(report_dir: str | None) -> dict:
    """Load per-trial JSON reports generated by generate_trial_reports.py.

    Returns dict mapping trace filename (e.g. M001_clock_abc123.jsonl) -> report dict.
    """
    reports = {}
    if not report_dir:
        return reports
    rd = Path(report_dir)
    if not rd.is_dir():
        print(f"Warning: report directory not found: {report_dir}", file=sys.stderr)
        return reports
    for rp in rd.glob("*.json"):
        try:
            with open(rp) as f:
                report = json.load(f)
            trace_file = report.get("trace_file", "")
            if trace_file:
                reports[trace_file] = report
        except (json.JSONDecodeError, KeyError):
            continue
    return reports


def extract_failure_info(report: dict) -> str:
    """Extract failure reason from a trial report.

    Returns formatted string: 'category | key_reason_zh' for failed trials,
    '-' for passed trials, 'N/A' if no report.
    """
    if not report:
        return "N/A"
    if report.get("status") == "succ":
        return "-"
    fc = report.get("failure_classification", {})
    category = fc.get("category", "")
    reason = fc.get("key_reason_zh", "")
    if category and reason:
        result = f"{category} | {reason}"
    elif category:
        result = category
    elif reason:
        result = reason
    else:
        reasons = report.get("failure_reason", [])
        if reasons:
            snippets = []
            for r in reasons:
                if isinstance(r, dict):
                    msg = r.get("reasoning", r.get("message", ""))
                    if msg:
                        snippets.append(str(msg)[:80])
            result = "; ".join(snippets[:2]) if snippets else "unknown"
        else:
            return "unknown"
    # Replace commas with semicolons to avoid CSV parsing issues
    return result.replace(",", ";").replace("，", "；")


def extract_trial_hash(trace_field: str) -> str:
    """Extract the hash part from a trace field.

    E.g. 'traces/.../M001_clock_8a61ea06.jsonl' -> '8a61ea06'
    Or 'M001_clock_8a61ea06.jsonl' -> '8a61ea06'
    """
    base = os.path.basename(trace_field).replace(".jsonl", "")
    parts = base.rsplit("_", 1)
    return parts[1] if len(parts) == 2 else ""


def fmt(val):
    """Format a numeric value, keeping reasonable precision."""
    if val is None:
        return "N/A"
    if isinstance(val, bool):
        return "Y" if val else "N"
    if isinstance(val, float):
        if val == int(val) and abs(val) < 1e6:
            return str(int(val))
        return f"{val:.2f}"
    return str(val)


def build_table(data, reports: dict | None = None):
    # Sort tasks alphabetically by task_id
    sorted_data = sorted(data, key=lambda t: t["task_id"])

    has_reports = bool(reports)

    # Column headers
    trial_cols = [
        "Trial", "Trial ID", "Input Toks", "Output Toks",
        "Model Time(s)", "Tool Time(s)", "Other Time(s)", "Wall Time(s)",
        "Completion", "Robustness", "Communication", "Safety",
        "Task Score", "Passed",
    ]
    header = ["Task ID", "Task Name", "Difficulty"] + trial_cols + ["Avg Score", "Pass@1", "PassHatK", "Overall"]
    if has_reports:
        header.append("Failure Reason")

    rows = [header]

    for task in sorted_data:
        task_id = task["task_id"]
        task_name = task["task_name"]
        difficulty = task["difficulty"]
        avg_score = task.get("avg_score")
        pass_at_1 = task.get("pass_at_1")
        pass_hat_k = task.get("pass_hat_k")
        avg_passed = task.get("avg_passed")

        trials = task.get("trials", [])
        for i, trial in enumerate(trials, 1):
            trace_field = trial.get("trace", "")
            trace_basename = os.path.basename(trace_field) if trace_field else ""
            trial_hash = extract_trial_hash(trace_field)
            # Look up report by basename (e.g. M001_clock_xxxx.jsonl)
            report = reports.get(trace_basename) if reports else None
            failure_info = extract_failure_info(report) if has_reports else ""

            row = [
                task_id if i == 1 else "",       # only show task_id on first sub-row
                task_name if i == 1 else "",
                difficulty if i == 1 else "",
                f"#{i}",
                trial_hash,
                fmt(trial.get("input_tokens")),
                fmt(trial.get("output_tokens")),
                fmt(trial.get("model_time_s")),
                fmt(trial.get("tool_time_s")),
                fmt(trial.get("other_time_s")),
                fmt(trial.get("wall_time_s")),
                fmt(trial.get("completion")),
                fmt(trial.get("robustness")),
                fmt(trial.get("communication")),
                fmt(trial.get("safety")),
                fmt(trial.get("task_score")),
                fmt(trial.get("passed")),
                fmt(avg_score) if i == 1 else "",
                fmt(pass_at_1) if i == 1 else "",
                fmt(pass_hat_k) if i == 1 else "",
                fmt(avg_passed) if i == 1 else "",
            ]
            if has_reports:
                row.append(failure_info)
            rows.append(row)

    return rows


def col_widths(rows):
    widths = [0] * len(rows[0])
    for row in rows:
        for i, cell in enumerate(row):
            widths[i] = max(widths[i], len(str(cell)))
    return widths


def render_table(rows):
    widths = col_widths(rows)
    sep = "+" + "+".join("-" * (w + 2) for w in widths) + "+"

    def format_row(row):
        return "|" + "|".join(f" {str(cell):<{widths[i]}} " for i, cell in enumerate(row)) + "|"

    lines = [sep, format_row(rows[0]), sep]
    for row in rows[1:]:
        lines.append(format_row(row))
        # Add separator after each task's last trial
        lines.append(sep)

    return "\n".join(lines)


def render_csv(rows):
    buf = io.StringIO()
    writer = csv.writer(buf)
    for row in rows:
        writer.writerow(row)
    return buf.getvalue()


def main():
    parser = argparse.ArgumentParser(description="Summarize batch evaluation results.")
    parser.add_argument("--input", "-i", default=None,
                        help="Path to batch_results.json (default: latest trace dir)")
    parser.add_argument("--config", "-c", default=None,
                        help="Path to claw-eval config YAML (resolves trace_dir/tasks_dir)")
    parser.add_argument("--report-dir", "-r", default=None,
                        help="Directory containing per-trial JSON reports (from generate_trial_reports.py)")
    parser.add_argument("-f", "--format", choices=["table", "csv"], default="table",
                        help="Output format (default: table)")
    parser.add_argument("-o", "--output", default=None,
                        help="Output file path (default: stdout)")
    args = parser.parse_args()

    input_file = resolve_input(args)
    print(f"Reading: {input_file}", file=sys.stderr)

    reports = load_reports(args.report_dir)
    if reports:
        print(f"Loaded {len(reports)} trial reports from: {args.report_dir}", file=sys.stderr)

    data = load_data(input_file)
    rows = build_table(data, reports)

    if args.format == "csv":
        output = render_csv(rows)
    else:
        output = render_table(rows)
        # Summary stats for table mode
        total_tasks = len(data)
        passed_tasks = sum(1 for t in data if t.get("avg_passed"))
        total_trials = sum(len(t.get("trials", [])) for t in data)
        passed_trials = sum(
            sum(1 for tr in t.get("trials", []) if tr.get("passed"))
            for t in data
        )
        output += f"\n\nSummary: {total_tasks} tasks, {passed_tasks} passed ({passed_tasks/total_tasks*100:.1f}%)\n"
        output += f"Trials: {total_trials} total, {passed_trials} passed ({passed_trials/total_trials*100:.1f}%)\n"

    if args.output:
        with open(args.output, "w") as f:
            f.write(output)
        print(f"Written to {args.output}")
    else:
        print(output)


if __name__ == "__main__":
    main()
